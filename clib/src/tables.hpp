// GF(256) arithmetic — primitive polynomial x^8+x^4+x^3+x^2+1 = 0x11D
inline constexpr std::array<uint8_t, 512> GF_EXP = []() {
    std::array<uint8_t, 512> t{};
    uint16_t x = 1;
    for (int i = 0; i < 256; ++i) {
        t[i] = t[i + 255] = static_cast<uint8_t>(x);
        x <<= 1;
        if (x & 0x100) x ^= 0x11D;
    }
    return t;
}();

inline constexpr std::array<uint8_t, 256> GF_LOG = []() {
    std::array<uint8_t, 256> t{};
    for (int i = 0; i < 255; ++i) t[GF_EXP[i]] = static_cast<uint8_t>(i);
    return t;
}();

inline constexpr uint8_t gf_mul(uint8_t a, uint8_t b) noexcept {
    if (a == 0 || b == 0) return 0;
    return GF_EXP[static_cast<int>(GF_LOG[a]) + static_cast<int>(GF_LOG[b])];
}

